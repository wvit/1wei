const interfaces = require('os').networkInterfaces();

let host = ''; //局域网地址
for (let devName in interfaces) {
  interfaces[devName].forEach(item => {
    if (item.family === 'IPv4' && item.address !== '127.0.0.1') {
      host = item.address;
    }
  })
}

